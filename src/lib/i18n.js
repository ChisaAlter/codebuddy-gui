const PACKS = { zh: ZH, en: EN };

export function translate(resolvedLocale, key, vars) {
  const pack = PACKS[resolvedLocale] || PACKS.zh;
  let text = pack[key] ?? PACKS.zh[key] ?? key;
  if (vars && typeof vars === 'object') {
    text = text.replace(/\{(\w+)\}/g, (_, name) => {
      const value = vars[name];
      return value == null ? `{${name}}` : String(value);
    });
  }
  return text;
}

export function applyDocumentLocale(resolvedLocale) {
  if (typeof document === 'undefined') return;
  document.documentElement.lang = resolvedLocale === 'en' ? 'en' : 'zh-CN';
}

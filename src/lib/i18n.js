const LOCALE_MODES = new Set(['zh', 'en', 'system']);

export function detectSystemLocale() {
  if (typeof navigator === 'undefined') return 'en';
  const candidates = [];
  if (Array.isArray(navigator.languages) && navigator.languages.length) {
    candidates.push(...navigator.languages);
  }
  if (navigator.language) candidates.push(navigator.language);
  for (const raw of candidates) {
    const code = String(raw || '').toLowerCase();
    if (code.startsWith('zh')) return 'zh';
  }
  return 'en';
}

export function resolveLocaleMode(mode) {
  const normalized = LOCALE_MODES.has(mode) ? mode : 'system';
  return normalized === 'system' ? detectSystemLocale() : normalized;
}

export function nextLocaleMode(mode) {
  // WebUI toggle: zh → en → system → zh
  if (mode === 'zh') return 'en';
  if (mode === 'en') return 'system';
  return 'zh';
}

export function nextThemeMode(mode) {
  // WebUI toggle: light → dark → system → light
  if (mode === 'light') return 'dark';
  if (mode === 'dark') return 'system';
  return 'light';
}

